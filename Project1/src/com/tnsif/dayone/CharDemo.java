package com.tnsif.dayone;

public class CharDemo {

	public static void main(String[] args) {
		
		char ch1='a';
		System.out.println(ch1);
		
		char ch2=65;
		System.out.println(ch2);
		
		char ch3='\u00A7';
		System.out.println(ch3);
		
		char ch4='\u20AC';
		System.out.println(ch4);
		
		int a='A';
		System.out.println(a);
		
		 
	}

}
